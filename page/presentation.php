				<h2 class="titrepresentation">Présentation de la sophrologie</h2>
			
				
				
				<div class="soustitre"><h3 class="toto">Historique</h3></div>
				<img src="img/sophro.png" style="float: left; width: 300px; height: 300px; padding-right: 20px;">
		 		<p>La sophrologie est née en 1960 à l’initiative du Professeur Alfonso Caycedo, neuropsychiatre d’origine colombienne exerçant en Espagne.</p>  
				<p>L’approche psychiatrique de l’époque est plutôt agressive.  Le professeur Caycedo propose une alternative en s’appuyant dans un premier temps sur des techniques occidentales de développement de la conscience comme l’hypnose thérapeutique, la relaxation progressive de Jacobson et le training autogène de Schulz.</p>
				
				<p>Pour étoffer sa méthode, il voyage en Orient et s’inspire des techniques de yoga, de méditation bouddhiste et de zen tout en les dépouillant de leurs aspects religieux.  C’est ainsi qu’est née la relaxation dynamique.
				En utilisant ces différents outils tant d’inspiration occidentale qu’orientale, il vise à donner à chacun la possibilité de développer sa conscience de lui-même et de son environnement.  Conscience qui lui permettra de vivre en harmonie.</p>
				<p>A l’origine essentiellement thérapeutique, la sophrologie a évolué vers des objectifs prophylactiques  et a ainsi fortement élargi ses champs d’application.</p>

				
				<div class="soustitre" style="margin-top: 200px;">
					<h3 class="toto">Champs d'application</h3>
				</div>
				
				<h4 class='h4'>Pour soulager :</h4>
				<ul>
					<li>Gestion du stress</li>
					<li>Apaisement des angoisses, des phobies, de l’inquiétude</li>
					<li>Prévention du burn-out (professionnel-parental)</li>
					<li>Gestion des émotions</li>
					<li>Gestion du poids, rapport à l’alimentation</li>
					<li>Gestion de la douleur : fibromyalgie, interventions médicales, maladies …</li>
					<li>Préparation à l’accouchement</li>
					<li>Retrouver un sommeil de qualité</li>
					<li>Se débarrasser des addictions : tabac, alcool, jeu, internet-réseaux sociaux, …</li>
					<li>...</li>
				</ul>
				<h4 class='h4'>Développement personnel :</h4>
				<ul>
					<li>Harmoniser corps, émotion et mental par une conscience de soi accrue</li>
					<li>Gagner en estime de soi et en confiance en soi</li>
					<li>Améliorer sa mémoire, sa concentration</li>
					<li>S’apprécier et apprécier ce qui nous entoure en posant un regard positif</li>
					<li>Se préparer aux évènements : examens, compétitions sportives, ….</li>
					<li>Transformer un passé encombrant en ressource pour un avenir constructif</li>
					<li>Développer sa capacité d’avoir des projets et de les réaliser</li>
					<li>...</li>
				</ul>	

				<div class="soustitre">
					<h3 class="toto">Déroulement d’une séance</h3>
				</div>

				<p>La sophrologie est une approche psychocorporelle du mieux-être.  Différentes étapes vont se succéder lors d’une séance afin de travailler la globalité de votre être ;</p>
				<p>Un échange sur les objectifs recherchés et le contenu de la séance.  A partir de la deuxième séance, un retour sur la séance précédente et les ressentis dans la vie quotidienne.</p>
				<p>Des exercices de respiration des mouvements vont induire un premier niveau de détente.</p>
				<p>Des techniques spécifiques vont vous aider à approfondir la détente et à prendre conscience des sensations envoyée par votre corps.</p>
				<p>Vient ensuite le moment où dans un état de détente profonde, une visualisation vous est proposée afin de vous permettre de poser un regard différent sur vos préoccupations qu’elles soient physiques, émotionnelles ou mentales et d’ainsi d’y apporter des solutions nouvelles.</p>
				<p>Pour terminer, tout en douceur vous revenez à votre état de vigilance habituel pour reprendre le fil habituel de vos activités mais vous serez enrichi de nouvelles capacités pour faire face aux évènements.</p>
				<p>Une description des ressentis de la séance pour une meilleure intégration des apports du travail effectué.</p>

				<div class="soustitre">
					<h3 class="toto">Les ateliers en groupes – s’accorder un moment pour une meilleure qualité de vie</h3>
				</div>

				<p>La sophrologie est accessible à tous et peut vous permettre de mieux vivre grâce à une méthode saine.
				Ces ateliers s’adressent à toute personne désirant s’accorder une pause, se détendre, lâcher-prise, améliorer son quotidien, découvrir et mieux utiliser ses ressources. </p>

				<h4 class='h4'>Les ateliers débutants </h4>

				<p>Je vous propose un atelier de 2x4 séances durant lesquelles vous découvrirez la technique qui vous permettra de faire face aux situations de stress qui se présentent dans votre vie quotidienne personnelle et/ou professionnelle.
				Chaque séance commence par des activités de prise de conscience du corps grâce aux mouvements doux de la relaxation dynamique et se poursuit par une visualisation guidée par ma voix</p>
				
				<h5 class='h5'>L’atelier découverte niveau 1 : 4 séances de prise en main </h5>


				<p>Redécouvrir sa respiration et l’utiliser pour s’apaiser.</p>
				<p>Ressentir son schéma corporel pour mieux le relâcher.</p>
				<p>Savourer l’instant présent.</p>
				<p>S’autoriser à penser à soi.</p>

				<h5 class='h5'>L’atelier découverte niveau 2 : 4 séances d’ancrage.</h5>

				<p>Prérequis : avoir suivi l’atelier découverte 1 ou avoir déjà pratiqué par ailleurs (sophrologie et relaxation dynamique)</p>

				<p>Développer son attention, sa concentration.</p>
				<p>Se protéger d’un environnement négatif.</p>
				<p>Garder son calme en toutes circonstances.</p>
				<p>Méditer avec les couleurs.</p>

				<h4 class='h4'>Les ateliers avancés </h4>
				
				<p>Vous avez participé aux ateliers découvertes 1 et 2 ou vous avez une expérience similaire de la sophrologie, alors ces ateliers vous sont destinés.</p>
				<p>A un rythme d’environ une fois par mois, vous explorez d’autres thèmes du mieux vivre, vous approfondissez la conscience de vous-même et de votre environnement, vous vous réconciliez avec des événements de votre passé et vous envisagez l’avenir avec sérénité et enthousiasme.</p>
				<p>En relaxation dynamique, vous avez l’occasion de continuer à pratiquer le premier degré et vous découvrez le deuxième degré.</p>

				<h4 class='h4'>Les ateliers à thème</h4>

				<p>Gestion du poids, sommeil, arrêt du tabac, préparation aux examens, …  différents ateliers centrés sur un objectif particulier sont organisés périodiquement, consultez notre agenda et/ou inscrivez-vous à notre newsletter pour être informé.</p>

				<h4 class='h4'>Les ateliers enfants et adolescents</h4>

				<p>Les enfants et les adolescents ne sont pas épargnés.  Ils ressentent également des moments de stress, exigences scolaires et parascolaires, examens, aléas du quotidien.</p>

				<p>Des séances de groupe adaptées sont proposées : </p>
				<ul>
					<li>De 6 à 12 ans</li>
					<li>De 12 à 18 ans</li>
				</ul>

				<div class="soustitre">
					<h3 class="toto">Les consultations individuelles – des contenus de séances personnalisés</h3>
					<p>Lors de la première rencontre, afin d’orienter et de personnaliser vos séances, nous examinerons ensemble votre ou vos objectif(s).</p>
					<p>Déroulement d’une séance (ici faire un lien vers le paragraphe au-dessus)</p>
					<p>Les visualisations seront adaptées à l’objectif et l’échange de fin de séance renforcera les prises de consciences apparues au cours de la séance.</p>
					<p>Un plan d’action à mettre en place jusqu’à la séance suivante sera établi en fin de séance.</p >
